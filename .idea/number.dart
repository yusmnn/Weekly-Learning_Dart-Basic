void main(List<String> args) {
  /**
    * number -> tipe data angka
    * hanya ada dua yaitu :
    * int untuk bilangn bulat
    * double untuk komma (memakai tanda titik)
  */

  int number1 = 10;
  double number2 = 3.14;
  print(number1 + number2);

  /**
   * num -> tipe data number yang bisa int ataupun double
   */
  num number3 = 2;
  print(number3);
  number3 = 0.1;
  print(number3);
}
