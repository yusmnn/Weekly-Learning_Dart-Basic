void main() {
  //operator Aritmatika
  double a = 20, b = 3;
  print(a + b);

  //Operator Pebandingan
  int c = 12, d = 10;
  print(c == d);

  //Operator penugasan:
  print(c += 15);

  //Operator logika
  bool e = true, f = false;
  print(e && f);

  print(e || f);

  // increment decrement
  int i = 3;
  print(++i);
  int j = 11;
  print(j--);
  print(j);
}
